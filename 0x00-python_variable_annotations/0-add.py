#!/usr/bin/env python3
"""
This module contains a function that adds two floats.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats.

    Parameters:
    a (float): The first float.
    b (float): The second float.

    Returns:
    float: The sum of the two floats.
    """
    return a + b
